#!/usr/bin/env python3
"""A module with basic annotations - add"""


def add(a: float, b: float) -> float:
    """
    A function that returns a sum as a float

    parameters:
    - a (float): The first float to be added.
    - b (float): The second float to be added.

    Returns:
    - The sum as a float.
    """
    return a + b
